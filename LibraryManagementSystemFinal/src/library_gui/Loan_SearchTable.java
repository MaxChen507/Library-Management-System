    // added rows from arraylist to jtable
    public void displayLoanToJTable()
    {
        DefaultTableModel model = (DefaultTableModel) loan_results_jTable.getModel();
        Object rowData[] = new Object[7];
        for(int i = 0; i < _list.size(); i++)
        {
            rowData[0] = _list.get(i)._Bname;
            rowData[1] = _list.get(i)._LoanId;
            rowData[2] = _list.get(i)._ISBN;
            rowData[3] = _list.get(i)._CardId;
            rowData[4] = _list.get(i)._DateOut;
            rowData[5] = _list.get(i)._DueDate;
            rowData[6] = _list.get(i)._DateIn;        
            model.addRow(rowData);
        }                
    }
    
