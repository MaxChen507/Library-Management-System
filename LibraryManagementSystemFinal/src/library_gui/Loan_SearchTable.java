    private void checkInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkInButtonActionPerformed
        String loanId_Str = loanIdTextField.getText();
        
        boolean check_in_flag = CheckInAPI.get_CheckIn_Conditions(loanId_Str);
        
        if (check_in_flag == false){
            JOptionPane.showMessageDialog(this, "Conditions for CheckIn not met", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            CheckInAPI.CheckOut_Done(loanId_Str);
            JOptionPane.showMessageDialog(this, "Loan with loan Id: " + loanId_Str + " checked in book");
        }
    }//GEN-LAST:event_checkInButtonActionPerformed

