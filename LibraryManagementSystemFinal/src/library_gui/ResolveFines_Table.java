     public void displayFinesToJTable()
    {
        DefaultTableModel model = (DefaultTableModel) ResolveFinesTable.getModel();
        Object rowData[] = new Object[2];
        for(int i = 0; i < _list.size(); i++)
        {
            rowData[0] = _list.get(i)._Card_id;
            rowData[1] = _list.get(i)._SumFineAmt;
            model.addRow(rowData);
        }                
    }
    
