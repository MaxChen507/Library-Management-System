    private void ResolveFineCard_idButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResolveFineCard_idButtonActionPerformed
        String Card_Id_Str = Card_idTextField.getText();
        
        boolean resolveFineCard_flag = ResolveFinesAPI.get_ResolveFines_CardID_ExistsCondition(Card_Id_Str);
        
        if (resolveFineCard_flag == false){
            JOptionPane.showMessageDialog(this, "Conditions for resolving fines not met", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            ResolveFinesAPI.ResolveFines_Done(Card_Id_Str);
            JOptionPane.showMessageDialog(this, "Borrower with card Id: " + Card_Id_Str + " resolved all their fines.");
        }
    }//GEN-LAST:event_ResolveFineCard_idButtonActionPerformed

